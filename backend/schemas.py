"""
schemas.py — Pydantic v2 request / response models.

Keeps API contracts separate from database models.
"""

import base64
import re
from datetime import datetime
from pydantic import BaseModel, EmailStr, Field, ConfigDict, field_validator

USERNAME_RE = re.compile(r"^[a-zA-Z0-9_.-]{3,64}$")


# ===========================================================================
# User schemas
# ===========================================================================

class UserCreate(BaseModel):
    """Payload for POST /users — registering a new account."""

    username: str = Field(..., min_length=3, max_length=64, examples=["alice"])
    email: EmailStr = Field(..., examples=["alice@example.com"])
    password: str = Field(..., min_length=8, max_length=128, examples=["s3cur3P@ss"])
    public_key: str | None = Field(
        default=None,
        description="PEM/base64-encoded public key for end-to-end encryption.",
    )

    @field_validator("username")
    @classmethod
    def _username_chars(cls, v: str) -> str:
        if not USERNAME_RE.match(v):
            raise ValueError("Username must be 3–64 characters: letters, digits, _ . - only.")
        return v


class UserLogin(BaseModel):
    """Payload for POST /auth/login."""

    # max_length caps are purely a DoS guard: an attacker sending a 10 MB
    # password string would force the server to run Argon2id over it (64 MB RAM
    # per attempt × unbounded string length). We do NOT add min_length here —
    # short/wrong credentials should fail with "Invalid credentials", not a
    # Pydantic 422, so we don't leak which field is wrong via the error path.
    username: str = Field(..., max_length=64)
    password: str = Field(..., max_length=128)

    @field_validator("username")
    @classmethod
    def _username_chars(cls, v: str) -> str:
        if not USERNAME_RE.match(v):
            raise ValueError("Username must be 3–64 characters: letters, digits, _ . - only.")
        return v


class UserResponse(BaseModel):
    """Safe user representation returned by the API (no password hash)."""

    model_config = ConfigDict(from_attributes=True)   # replaces orm_mode in Pydantic v2

    id: int
    username: str
    email: EmailStr
    public_key: str | None
    is_active: bool
    created_at: datetime


class OnChainKeyInfo(BaseModel):
    """KeyRegistry.getKey() result for one identity, plus the DB's pointer
    to the most recent registration/rotation transaction (informational —
    the contract's event log remains authoritative)."""

    registered: bool
    version: int
    key_b64: str | None
    updated_at: int | None
    revoked: bool
    tx_hash: str | None = None


class UserPublicKeyResponse(BaseModel):
    """Minimal user record for public key discovery.

    Intentionally excludes email and all other PII — this schema is used by
    unauthenticated endpoints (GET /users, GET /users/{username}) whose sole
    purpose is to let a sender look up a recipient's HPKE public key.
    Returning email from an endpoint that requires no authentication would
    allow any anonymous caller to harvest the full user list with emails.
    """

    model_config = ConfigDict(from_attributes=True)

    id: int
    username: str
    public_key: str | None

    # Populated only when the caller passes ?onchain=1 — a live KeyRegistry
    # read, not a DB lookup, so a client can catch a live discrepancy
    # between what the server says and what the chain says. None when not
    # requested; also None (with onchain_error set) if the RPC call fails —
    # this endpoint never fails the whole response over a chain read.
    onchain: OnChainKeyInfo | None = None
    onchain_error: str | None = None


class PublicKeyUpload(BaseModel):
    """Body for POST /users/keys — upload or replace the caller's HPKE public key.

    The public key is the X25519 static public key generated by
    ``generate_keypair()``.  It is stored in ``users.public_key`` and
    returned by the key-discovery endpoints so other users can encrypt
    messages to this user with HPKE Mode_Auth.
    """

    # The raw 32-byte X25519 public key, base64-encoded.
    # Standard base64 of 32 bytes is exactly 44 characters (including padding).
    # URL-safe base64 is also accepted (base64.b64decode handles both when
    # validate=True is not strict about the alphabet, but we use standard
    # base64 throughout this project for consistency).
    public_key: str = Field(
        ...,
        description=(
            "Base64-encoded 32-byte X25519 public key produced by "
            "generate_keypair().  Must decode to exactly 32 bytes."
        ),
    )

    @field_validator("public_key")
    @classmethod
    def _must_decode_to_32_bytes(cls, v: str) -> str:
        # Step 1: valid base64 encoding
        try:
            raw = base64.b64decode(v, validate=True)
        except Exception:
            raise ValueError("public_key must be valid base64.")
        # Step 2: correct length for an X25519 key (HPKE_KEY_SIZE = 32)
        if len(raw) != 32:
            raise ValueError(
                f"public_key must decode to exactly 32 bytes (X25519 / HPKE); "
                f"got {len(raw)}."
            )
        return v


